public class Solution283 {

    public void moveZeroes(int[] nums) {
        int fast = 0;
        int slow = 0;
        while (fast < nums.length) {
            if (nums[fast] != 0) {
                nums[slow] = nums[fast];
                slow++;
            }
            fast++;
        }
        for (int i = slow + 1; i < nums.length; i++) {
            nums[i] = 0;
        }
    }
}
